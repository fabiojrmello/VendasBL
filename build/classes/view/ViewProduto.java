    private void jbExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbExcluirActionPerformed
        // EXCLUI UM NOVO PRODUTO NO BANCO
        int linha = jtableProdutos.getSelectedRow();
        int codigoProduto = (int) jtableProdutos.getValueAt(linha, 0); //getValueAt(linha, 0) ele vai pegar o valor da linha e coluna que estiver selecionada, (int)vai converter o valor que a mesma receber para inteiro.
        
        if (controllerProdutos.excluirProdutoController(codigoProduto)){ //Esse If já retornana o valor verdadeiro (true)
            JOptionPane.showMessageDialog(this, "Produto excluido com sucesso!", "ATENÇÃO", JOptionPane.WARNING_MESSAGE); //WARNING_MESSAGE, mensagem de alerta
            this.carregarProdutos(); // carregar a lista de produto para atualizar
            this.habilitarDesabilitarCampos(false);
        }else{
            JOptionPane.showMessageDialog(this, "Erro ao excluir o produto!", "ERRO", JOptionPane.ERROR_MESSAGE);//ERROR_MESSAGE, menagem de erro
        }
    }//GEN-LAST:event_jbExcluirActionPerformed

