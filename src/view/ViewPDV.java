    private void salvarVenda() {
        // TODO add your handling code here:
        int cont;
        int codigoProduto = 0, codigoVenda = 0;
        modelVendas = new ModelVendas(); // Declarar o modelVenda para não pegar nenhum lixo da memoria
        modelVendas.setCliente(1);// Aqui eu estou definindo o codigo sempre para o usuario 1
        try {
            modelVendas.setVenDataVenda(bLDatas.converterDataParaDateUS(new java.util.Date(System.currentTimeMillis())));// ele esta convertendo para data US e pegando a data do sistema
        } catch (Exception ex) {
            Logger.getLogger(ViewPDV.class.getName()).log(Level.SEVERE, null, ex);
        }

        modelVendas.setVenValorBruto(Double.parseDouble(jtfValorBruto.getText()));
        modelVendas.setVenDesconto(viewPagamentoPDV.getDesconto());
        modelVendas.setVenValorLiquido(blm.arredondamentoComPontoDuasCasas(viewPagamentoPDV.getValorTotal()));

        //Salvar venda
        codigoVenda = controllerVendas.salvarVendasController(modelVendas);
        //Salvando e listando os itens da lista
        cont = jtProdutos.getRowCount();// Está pegando a quantidade de itens da minha lista.
        for (int i = 0; i < cont; i++) {
            codigoProduto = (int) jtProdutos.getValueAt(i, 1);
            modelVendasProdutos = new ModelVendasProdutos();
            modelProdutos = new ModelProdutos();
            modelVendasProdutos.setProduto(codigoProduto);
            modelVendasProdutos.setVendas(codigoVenda);
            modelVendasProdutos.setVenProValor((double) jtProdutos.getValueAt(i, 4));
            modelVendasProdutos.setVenProQuantidade(Integer.parseInt(jtProdutos.getValueAt(i, 3).toString()));
            //Produto
            modelProdutos.setIdProduto(codigoProduto);
            modelProdutos.setProEstoque(controllerProdutos.retornarProdutoController(codigoProduto).getProEstoque()
                    - Integer.parseInt(jtProdutos.getValueAt(i, 3).toString()));
            listaModelVendasProdutoses.add(modelVendasProdutos);
            listaModelProdutoses.add(modelProdutos);
        }

        // Salvar os produtos da venda
        if (controllerVendasProdutos.salvarVendasProdutosController(listaModelVendasProdutoses)) {
            //Alterar o estoque de produtos
            controllerProdutos.alterarEstoqueProdutoController(listaModelProdutoses);
            this.limparTela();
        } else {
            JOptionPane.showMessageDialog(this, "Erro ao salvar os produtos!", "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }

    /**
     * Efetuar a limpeza dá tela após terminar a venda
     */
    private void limparTela() {
        jtfValorBruto.setText("");
        DefaultTableModel modelo = (DefaultTableModel) jtProdutos.getModel();
        modelo.setNumRows(0);
        jlStatus.setText("Caixa Livre");
    }

    //Ele vai pegar os dados que eu digitar e quando eu aperta ENTER ele vai coletar os dados
    private void pegarConteudo(java.awt.event.KeyEvent e) {
        jlStatus.setText("Venda em aberto");
        DefaultTableModel modelo = (DefaultTableModel) jtProdutos.getModel();
        if (e.getKeyCode() == java.awt.event.KeyEvent.VK_ENTER) {
            try {
                modelProdutos = controllerProdutos.retornarProdutoController(Integer.parseInt(jtfCodigoProduto.getText()));
                modelo.addRow(new Object[]{
                    modelo.getRowCount() + 1, // Aqui ele esta contando a quantidade de linha da tabela
                    modelProdutos.getIdProduto(),
                    modelProdutos.getProNome(),
                    quantidade, // essa quantidade vai receber o valor que a gente digitar na caixa
                    modelProdutos.getProValor(),
                    modelProdutos.getProValor() * quantidade
                });
                jtfValorBruto.setText(String.valueOf(somaValorTotal()));
                jtfCodigoProduto.setText("");//Vou limpar a minha tela
                quantidade = 1;
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Você  deve informar apenas numeros neste campo!", "Erro", JOptionPane.ERROR_MESSAGE);
            }
        }
    }

    //Ele vai pegar os valores da comprar, vai somar e dar o resultado do valor total da comprar
    private float somaValorTotal() {
        float soma = 0, valor = 0;
        int cont = jtProdutos.getRowCount(); //Pegar as quantidade de linha
        for (int i = 0; i < cont; i++) {
            valor = Float.parseFloat(String.valueOf(jtProdutos.getValueAt(i, 5)));// Esta percorrendo na tabela e pegando os dados na coluna "Valor Total"
            soma += valor; // Estou somando o valor da comprar e retornando o valor total da comprar para o Soma.
        }

        return soma;
    }

