    private void entrar() {
        modelUsuario.setUsuLogin(jtfLogin.getText());//Preencher o model usuario, pegar dados do login.
        modelUsuario.setUsuSenha(String.valueOf(jtfSenha.getPassword()));//Preencher o model usuario, pegar dados da senha, converter para string.
        if (controllerUsuario.getValidarUsuarioController(modelUsuario)) {
            modelUsuario = controllerUsuario.getUsuarioController(jtfLogin.getText());
            modelSessaoUsuario.codigo = modelUsuario.getIdUsuario();
            modelSessaoUsuario.nome = modelUsuario.getUsuNome();
            modelSessaoUsuario.login = modelUsuario.getUsuLogin();
            new ViewPrincipal().setVisible(true);//Se for verdadeiro ele vai chama a interface principal
        } else {
            JOptionPane.showMessageDialog(this, "Usu√°rio/Senha inv√°lida!", "ATEN√á√ÉO", JOptionPane.WARNING_MESSAGE); //WARNING_MESSAGE, mensagem de alerta
        }
    }

